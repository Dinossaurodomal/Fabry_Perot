import numpy as np
import math
import matplotlib.pyplot as plt

#--------------------------------------------------------------------------------------------------------------------#
#definindo par√¢metros gerais
freq = 100*10^9
T = 1/freq
w = 2*np.pi*freq
lamb = (3*10^8)/freq
beta = 2*np.pi/lamb
tempo = np.arange(0,100,1)
#--------------------------------------------------------------------------------------------------------------------#
#definindo E1
z1 = 10
e = 100
E1 = e*(np.cos((w*tempo)-(beta*z1)))
#--------------------------------------------------------------------------------------------------------------------#
#definindo E2
z2 = 15
E2 = e*(np.cos((w*tempo)-(beta*z2)))
#--------------------------------------------------------------------------------------------------------------------#
#definindo E0
E0 = E1+E2

fig, ax = plt.subplots()
ax.plot(tempo, E0)
plt.show()